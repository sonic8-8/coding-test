        while (input.length() != N) {
            input = sc.nextLine();
        }

        int total = 0;

        for (int i = 0 ; i < input.length() ; i++) {
            char temp = input.charAt(i);
            int num = Character.getNumericValue(temp);
            total += num;
        }

        System.out.println(total);

