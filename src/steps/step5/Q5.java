        // 문제점 ( 주의!!!! 문제 잘못읽고 푼 풀이임. 쭉 내리면 제대로 푼 거 나옴)
        // 공백없이 입력받은 숫자인데 어떻게 1자리 숫자인지 2자리 숫자인지를 확인?
        //      (1) N이 3이라면 둘째줄에 입력받을 숫자를 10 * 2 으로 나누기
        //          몫이 10보다 크다면 11 10 이런식으로 숫자가 나열되어있을 것임
        //          12 11 10
