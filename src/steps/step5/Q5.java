        //          숫자 3개인데 5자리 숫자?
        //          한자리 숫자라면 3이어야하는데 5자리라면 2자리가 남음
        //          경우의 수 :
        //          맨앞숫자가 3자리 숫자 ( 100보다 크면 X 먼저 확인 후 다음으로 진행 )
        //          맨앞 숫자 2자리 두번째 숫자 2자리

        // 1자리 숫자인지 2자리 숫자인지 확인하더라도 이를 배열에 넣어야할 필요가 있을까? 바로 더하면 되는거 아님?...

        // 문제를 잘못 이해해서 어렵게 풀고 있었다.
