        // 2. 개수만큼 더하기

        Scanner sc = new Scanner(System.in);
        int N = sc.nextInt();
        if (N < 0 || N > 100) {
            N = sc.nextInt();
        }

        // ----------------------------------------------------------------------------------------------------

        // 이거 아님.. ㅠㅠ
